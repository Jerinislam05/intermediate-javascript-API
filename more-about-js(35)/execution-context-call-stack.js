// Execution Context

/* 
1. An execution context is an environment.

2. inside the execution context a piece of JavaScript code gets executed

3. Variable, parameters and information related to the piece of code get stored in the execution context.

4. There are two kinds of execution context in JavaScript:
** Global Execution Context(GEC) and Function Execution Context(FEC).

5. The execution context is created in two phases: 
->Creation phase and ->Execution phase.
*/